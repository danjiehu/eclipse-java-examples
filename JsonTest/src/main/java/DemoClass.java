
public class DemoClass {

	private int id;
	private String name;
	private boolean active;
	
	public void setId(int i) {
		id = i;
	}
	
	public void setName(String s) {
		name = s;
	}
	
	public void setActive(boolean b) {
		active = b;
	}
	
	public int getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public boolean getActive() {
		return active;
	}
}

